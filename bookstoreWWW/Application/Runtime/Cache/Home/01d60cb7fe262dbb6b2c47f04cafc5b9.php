1. 名词定义
1.1 尚品联盟合作：您在您网站上设置至尚品工艺网站的推广链接，为尚品工艺网站进行广告推广，以促进尚品工艺商品的销售，尚品工艺通过您的推广达成的有效购买行为向您支付佣金的合作方式。
1.2 您网站：指您拥有和运营的，链接到尚品工艺网站的任何网站或软件应用程序，以及您实施尚品联盟合作的其他渠道（包括但不限于论坛、SNS）
1.3 尚品工艺网站：指域名为www.shangpin.com的网站。
1.4 推广链接：指您根据本协议约定在您网站上设置的与尚品工艺网站的链接，该链接应使用尚品工艺提供的特定跟踪链接格式。
1.5 用户：指通过您网站中的推广链接访问尚品工艺网站，并购买尚品工艺返佣商品的组织和/或个人。
1.6 佣金：指尚品工艺就合作中尚品工艺返佣商品的销售，向您支付的服务费用。
1.7 尚品工艺返佣商品：指尚品工艺网站上销售的可以根据本协议约定计算佣金的商品，与尚品联盟返佣的特例商品。
1.8 净销售额：指用户购买尚品工艺返佣商品时实际支付的金额，不包含优惠券金额、礼品卡金额、运费金额以及云钻等。
1.9 尚品联盟平台服务费：推客使用尚品联盟平台服务而向尚品工艺支付的服务费用。尚品联盟平台服务费=有效订单净销售额×佣金比例×10%。
1.10 广告效果认定期和有效购买行为
1.10.1 广告效果认定期，指自用户通过点击您网站上的推广链接访问尚品工艺网站开始，至下述最先发生的时间止：
（1） 用户初次点击后24小时；
（2） 用户对尚品工艺返佣商品下订单；
（3） 用户从其他链接访问或直接输入网址进入尚品工艺网站。
1.10.2 有效购买行为，指满足下述两个条件的购物行为：
（1）用户通过您网站的推广链接访问尚品工艺网站；
（2）在广告效果认定期间里，将尚品工艺返佣商品放入其购物车并对该商品下订单或者通过一键购功能购买返佣商品。
1.10.3 下列任何一种情况不构成有效购买行为，尚品工艺不会就此向您支付佣金：
（1）用户从您网站上的推广链接访问尚品工艺网站，在广告效果认定期结束后将尚品工艺返佣商品加入购物车或者通过一键购功能进行下单购买；
（2）由于您网站的推广链接格式不正确而导致未能正确跟踪的购买行为；
（3）用于再度销售或任何性质的商业用途的订单；
（4）代购、批发、闪拍、拍卖、SWL类订单；
（5）在本协议终止后通过推广链接购买的尚品工艺返佣商品；
（6）订单被撤销或退货；
（7）开具增值税专用发票的订单；
（8）选购过程中出现意外关机或者访问其他网站等情况，造成用户cookies记录丢失的订单；
（9）选购过程中，使用校园代理账户或者尚品工艺其他相关代理业务的账号作为支付账号；
（10）直接推广尚品工艺首页，由此产生的订单。
（11）尚品企业用户所带来的订单。
