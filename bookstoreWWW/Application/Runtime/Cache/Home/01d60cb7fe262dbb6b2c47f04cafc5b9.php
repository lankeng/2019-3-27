（2） 宣扬暴力或含有暴力内容；
（3） 宣扬或含有诽谤性或侮辱性内容；
（4） 宣扬基于种族、性别、宗教信仰、国籍、残疾、性取向或年龄等的歧视，或实施上述歧视性行为；
